def _haversine_m(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    R = 6_371_000
    la1, la2 = math.radians(lat1), math.radians(lat2)
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat / 2) ** 2 + math.cos(la1) * math.cos(la2) * math.sin(dlon / 2) ** 2
    return 2 * R * math.asin(math.sqrt(a))


def dist_to_seoul(city: str) -> float | None:
    """도시/IC 이름 → 서울까지 직선거리(m). 미등록 시 None."""
    city = city.strip()
    if city in CITY_COORDS:
        lat, lon = CITY_COORDS[city]
        return _haversine_m(lat, lon, *_SEOUL)
    # 부분 일치 (예: '광주광역시' ↔ '광주')
    for key, (lat, lon) in CITY_COORDS.items():
        if key in city or city in key:
            return _haversine_m(lat, lon, *_SEOUL)
    return None


def direction_from_cities(start_city: str, end_city: str, threshold_m: float = 30_000) -> str | None:
    """
    'A기점 + B종점' 에서 start=A, end=B.
    dist(B, 서울) < dist(A, 서울) by >threshold → 상행 (B가 더 서울에 가까움)
    dist(A, 서울) < dist(B, 서울) by >threshold → 하행 (A가 더 서울에 가까움)
    그 외 → None (양방향 또는 불명확)
    """
    d_s = dist_to_seoul(start_city)
    d_e = dist_to_seoul(end_city)
    if d_s is None or d_e is None:
        return None
    diff = d_s - d_e          # >0: end가 서울에 더 가까움
    if diff > threshold_m:
        return "상행"
    if diff < -threshold_m:
        return "하행"
    return None


def parse_route_direction(raw: str) -> str | None:
    """
    '통영기점 + 하남종점' 형식 문자열 → '상행' / '하행' / None.
    """
    raw = raw.strip()
    m = re.match(r"(.+?)기점\s*\+\s*(.+?)종점", raw)
    if not m:
        return None
    return direction_from_cities(m.group(1).strip(), m.group(2).strip())


def parse_name_direction(name: str) -> str | None:
    """
    휴게소명 '문막(인천)' '매송(목포)' '오산(상행)' → '상행' / '하행' / None.
    """
    # 명시적 상행/하행
    if "상행" in name:
        return "상행"
    if "하행" in name:
        return "하행"
    # 방면 키워드
    for kw in ("서울방면", "서울방향", "수도권"):
        if kw in name:
            return "상행"
    # 괄호 안 도시명
    m = re.search(r"\(([^)]+)\)", name)
    if not m:
        return None
    city = re.sub(r"(방면|방향)$", "", m.group(1)).strip()
    d = dist_to_seoul(city)
    if d is None:
        return None
    # 서울 150km 이내 도시가 종점이면 상행, 250km 이상이면 하행
    if d < 150_000:
        return "상행"
    if d > 220_000:
        return "하행"
    return None
